/// Types of pieces that exist in chess.
public enum ChessPieceType: String {

    /// King piece
    case king = "K"

    /// Queen piece
    case queen = "Q"

    /// Bishop piece
    case bishop = "b"

    /// Knight piece
    case knight = "k"

    /// Rook piece
    case rook = "r"

    /// Pawn piece
    case pawn = "p"

}
